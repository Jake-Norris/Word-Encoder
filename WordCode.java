public class WordCode {
	
	private char letter;
	private String code;
	
	public WordCode( char c, String s) {
		letter = c;
		code = s;
	}
	
	public char getLetter() {
		return letter;
	}
	
	public String getCode() {
		return code;
	}
	
	
}
